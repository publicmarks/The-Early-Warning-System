import nltk
from nltk.corpus import stopwords
from nltk.stem import WordNetLemmatizer
from sklearn.feature_extraction.text import TfidfVectorizer
from imblearn.over_sampling import SMOTE
from collections import Counter

nltk.download('stopwords')
nltk.download('wordnet')

stop_words = set(stopwords.words('english'))
lemmatizer = WordNetLemmatizer()

def preprocess_text(text):
    text = text.lower() # Lowercasing
    text = ''.join([char for char in text if char.isalnum() or char.isspace()]) # Punctuation removal (keep alphanumeric and spaces)
    tokens = text.split() # Tokenization
    tokens = [word for word in tokens if word not in stop_words] # Stop-word filtering
    tokens = [lemmatizer.lemmatize(word) for word in tokens] # Lemmatization
    return ' '.join(tokens)

print('Applying text preprocessing...')
df['processed_text'] = df['usertext'].apply(preprocess_text)
print('Text preprocessing complete.')

# TF-IDF Vectorization
