import tensorflow as tf
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score, roc_auc_score
from sklearn.utils import class_weight
from transformers import AutoTokenizer, TFAutoModelForSequenceClassification

# Split the preprocessed text data for BERT (using the original processed_text and labels)
# Note: X_resampled from SMOTE was for TF-IDF, BERT requires raw text input.
print('Splitting original processed text data into training and testing sets for BERT...')
X_train_bert, X_test_bert, y_train_bert, y_test_bert = train_test_split(
    df['processed_text'].tolist(),
    df['label'].tolist(),
    test_size=0.2,
    random_state=42,
    stratify=df['label'].tolist() # Stratify to maintain class distribution
)
print(f'BERT Data split complete. Training samples: {len(X_train_bert)}, Testing samples: {len(X_test_bert)}')

# Load pre-trained tokenizer
print('\nLoading BERT tokenizer...')
tokenizer = AutoTokenizer.from_pretrained('bert-base-uncased')
print('Tokenizer loaded.')

# Tokenize the data
print('Tokenizing training data...')
train_encodings = tokenizer(X_train_bert, truncation=True, padding=True, max_length=128, return_tensors='tf')
print('Tokenizing testing data...')
test_encodings = tokenizer(X_test_bert, truncation=True, padding=True, max_length=128, return_tensors='tf')
print('Data tokenization complete.')

# Create TensorFlow Datasets
train_dataset = tf.data.Dataset.from_tensor_slices((
    dict(train_encodings),
    tf.constant(y_train_bert)
)).batch(16)

test_dataset = tf.data.Dataset.from_tensor_slices((
    dict(test_encodings),
    tf.constant(y_test_bert)
)).batch(16)

# Calculate class weights for imbalanced dataset during BERT training
print('\nCalculating class weights...')
class_weights = class_weight.compute_class_weight('balanced', classes=np.unique(y_train_bert), y=y_train_bert)
class_weight_dict = {i: weight for i, weight in enumerate(class_weights)}
print('Class weights calculated:', class_weight_dict)

# Load pre-trained BERT model for sequence classification
print('\nLoading BERT model...')
model = TFAutoModelForSequenceClassification.from_pretrained('bert-base-uncased', num_labels=2)
print('BERT model loaded.')

# Compile the model
optimizer = tf.keras.optimizers.Adam(learning_rate=5e-5)
loss = tf.keras.losses.SparseCategoricalCrossentropy(from_logits=True)
metrics = [tf.keras.metrics.SparseCategoricalAccuracy('accuracy')]

model.compile(optimizer=optimizer, loss=loss, metrics=metrics)

# Train the model
print('\nTraining BERT model...')
# Convert class_weight_dict to a dict usable by Keras for class_weight argument
# Keras expects dictionary keys to be integers representing class labels.
# Our class_weight_dict already matches this format.
model.fit(
    train_dataset,
    epochs=3, # You can adjust the number of epochs
    batch_size=16,
    class_weight=class_weight_dict
)
print('BERT model training complete.')

# Evaluate the BERT model
print('\nEvaluating BERT model...')
predictions = model.predict(test_dataset)
logits = predictions.logits
y_pred_bert = np.argmax(logits, axis=1)
y_proba_bert = tf.nn.softmax(logits, axis=1)[:, 1].numpy()

print('\nBERT Model Evaluation:')
print(f'Accuracy: {accuracy_score(y_test_bert, y_pred_bert):.4f}')
print(f'Precision: {precision_score(y_test_bert, y_pred_bert):.4f}')
print(f'Recall: {recall_score(y_test_bert, y_pred_bert):.4f}')
print(f'F1-score: {f1_score(y_test_bert, y_pred_bert):.4f}')
print(f'ROC-AUC: {roc_auc_score(y_test_bert, y_proba_bert):.4f}')

